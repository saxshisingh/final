class BinanceCoinWallet {
    constructor() {        
        this.contractAddress = '0x6183c64a3d72688cd9c44adb2efc8fd94d769916'; 
        this.contract = new web3.eth.Contract(contractABI, this.contractAddress);
        this.wallet = this.generateUserWallet(); 
    }

    generateUserWallet() {
        const wallet = Wallet.generate(); 
        return wallet.getAddressString(); 
    }

    async getBalance(address) {
        try {
            const balanceWei = await web3.eth.getBalance(address);
            return web3.utils.fromWei(balanceWei, "ether"); 
        } catch (error) {
            console.error('Error getting balance:', error);
            return 0; 
        }
    }

    async deposit(amount) {
        try {
            const userWallet = this.generateUserWallet();
            const balance = await this.getBalance(this.wallet);
            if (balance < amount) {
                console.error('Insufficient balance in user wallet:', balance, 'BNB');
                return;
            }
            const amountWei = web3.utils.toWei(amount.toString(), 'ether');
            const tx = await this.contract.methods.transfer(userWallet, amountWei).send({ from: this.wallet });
            console.log('Deposit successful:', tx);
            console.log('Deposited', amount, 'BNB to', userWallet);
        } catch (error) {
            console.error('Error depositing:', error);
        }
    }

    async withdraw(amount, toAddress) {
        try {
            const contractBalance = await this.getBalance(toAddress);
            if (contractBalance < amount) {
                console.error('Insufficient balance in recipient address:', contractBalance, 'BNB');
                return;
            }
            const amountWei = web3.utils.toWei(amount.toString(), 'ether');
            const tx = await this.contract.methods.transfer(this.wallet, amountWei).send({ from: toAddress });
            console.log('Withdrawal successful:', tx);
            console.log('Withdrawn', amount, 'BNB to', this.wallet);
        } catch (error) {
            console.error('Error withdrawing:', error);
        }
    }
}



