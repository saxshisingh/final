    async getBalance(address) {
        try {
            const balanceWei = await web3.eth.getBalance(address);
            return web3.utils.fromWei(balanceWei, "ether"); 
        } catch (error) {
            console.error('Error getting balance:', error);
            return 0;
        }
    }

    async deposit(amount) {
        try {
            const contractBalance = await this.getBalance(this.walletAddress);
            if (contractBalance < amount) {
                console.error('Insufficient balance in contract:', contractBalance, 'ETH');
                return;
            }
            const recipientWallet = web3.eth.accounts.create();
            const amountWei = web3.utils.toWei(amount.toString(), 'ether');
            const tx = await this.contract.methods.transfer(recipientWallet.address, amountWei).send({ from: this.walletAddress });
            console.log('Deposit successful. Transferred', amount, 'ETH to:', recipientWallet.address);
        } catch (error) {
            console.error('Error depositing funds:', error);
        }
    }

    async withdraw(amount, toAddress) {
        try {
            const contractBalance = await this.getBalance(toAddress);
            if (contractBalance < amount) {
                console.error('Insufficient balance in recipient address:', contractBalance, 'ETH');
                return;
            }
            const amountWei = web3.utils.toWei(amount.toString(), 'ether');
            const tx = await this.contract.methods.transfer(this.walletAddress, amountWei).send({ from: toAddress });
            console.log('Withdrawal successful. Transferred', amount, 'ETH to:', this.walletAddress);
        } catch (error) {
            console.error('Error withdrawing funds:', error);
        }
    }
